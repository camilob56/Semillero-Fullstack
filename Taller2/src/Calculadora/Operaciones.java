package Calculadora;

public class Operaciones {
    
    public static int Sumar(int a, int b) {
        return a + b;
    }

    public static int Restar(int a, int b) {
        return a - b;
    }

    public static int Multiplicar(int a, int b) {
        return a * b;
    }

    public static double Dividir(double a, double b) {
        return a/b;
    }
}
